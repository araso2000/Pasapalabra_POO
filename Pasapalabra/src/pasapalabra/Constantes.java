package pasapalabra;

public class Constantes {

	private int numero;
	private char letra;
	
	public Constantes(int numero,char letra) {
		this.numero=numero;
		this.letra=letra;
	}
	
	public int getNumero() {
		return(this.numero);
	}
	
	public char getLetra() {
		return(this.letra);
	}
}
